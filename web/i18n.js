  function normalizeLocale(locale) {
    return SUPPORTED.has(locale) ? locale : DEFAULT_LOCALE;
  }

  function getLocale() {
    try {
      return normalizeLocale(localStorage.getItem(STORAGE_KEY) || DEFAULT_LOCALE);
    } catch {
      return DEFAULT_LOCALE;
    }
  }

  function persistLocale(locale) {
    try {
      localStorage.setItem(STORAGE_KEY, normalizeLocale(locale));
    } catch {
      /* private mode */
    }
  }

  function interpolate(template, params) {
    return String(template).replace(/\{([a-zA-Z0-9_]+)\}/g, (_, key) => {
      return Object.prototype.hasOwnProperty.call(params || {}, key) ? String(params[key]) : `{${key}}`;
    });
  }

  function t(key, params = {}, locale = getLocale()) {
    const lang = normalizeLocale(locale);
    const value = messages[lang]?.[key] ?? messages.fr[key];
    return value ? interpolate(value, params) : key;
  }

  function translateElement(node, locale = getLocale()) {
    const textKey = node.getAttribute?.("data-i18n");
    if (textKey) node.textContent = t(textKey, {}, locale);
    const placeholderKey = node.getAttribute?.("data-i18n-placeholder");
    if (placeholderKey) node.setAttribute("placeholder", t(placeholderKey, {}, locale));
    const ariaKey = node.getAttribute?.("data-i18n-aria-label");
    if (ariaKey) node.setAttribute("aria-label", t(ariaKey, {}, locale));
    const titleKey = node.getAttribute?.("data-i18n-title");
    if (titleKey) node.setAttribute("title", t(titleKey, {}, locale));
  }

  function updateLocaleButtons(locale) {
    document.querySelectorAll?.("[data-i18n-locale]").forEach((button) => {
      const active = button.getAttribute("data-i18n-locale") === locale;
      button.setAttribute("aria-pressed", active ? "true" : "false");
      button.classList?.toggle("is-active", active);
    });
  }

  function applyTranslations(root = document) {
    const locale = getLocale();
    if (document.documentElement) document.documentElement.lang = locale;
    root.querySelectorAll?.("[data-i18n]").forEach((node) => translateElement(node, locale));
    root.querySelectorAll?.("[data-i18n-placeholder]").forEach((node) => translateElement(node, locale));
    root.querySelectorAll?.("[data-i18n-aria-label]").forEach((node) => translateElement(node, locale));
    root.querySelectorAll?.("[data-i18n-title]").forEach((node) => translateElement(node, locale));
    updateLocaleButtons(locale);
  }

  function notify(locale) {
    for (const cb of listeners) {
      try {
        cb(locale);
      } catch {
        /* isolate subscriber failures */
      }
    }
    try {
      dispatchEvent(new CustomEvent("pokevault:localechange", { detail: { locale } }));
    } catch {
      /* tests or old browsers */
    }
  }

  function setLocale(locale) {
    const next = normalizeLocale(locale);
    persistLocale(next);
    applyTranslations();
    notify(next);
  }

  function subscribeLocale(cb) {
    listeners.add(cb);
    return () => listeners.delete(cb);
  }

  function wireLocaleButtons() {
    document.querySelectorAll?.("[data-i18n-locale]").forEach((button) => {
      if (button.dataset?.i18nWired) return;
      if (button.dataset) button.dataset.i18nWired = "1";
      button.addEventListener?.("click", () => setLocale(button.getAttribute("data-i18n-locale")));
    });
  }

  function boot() {
    wireLocaleButtons();
    applyTranslations();
  }

  window.PokevaultI18n = {
    getLocale,
    setLocale,
    subscribeLocale,
    t,
    applyTranslations,
    translateElement,
    _test: { messages, normalizeLocale, interpolate }
  };

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", boot, { once: true });
  } else {
    boot();
  }
